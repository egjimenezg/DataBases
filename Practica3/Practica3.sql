CREATE TABLE CLIENTE(
	ID_CLIENTE INTEGER PRIMARY KEY AUTO_INCREMENT,
	NOMBRE VARCHAR(30) NOT NULL, 
	APPATERNO VARCHAR(30) NOT NULL,
	APMATERNO VARCHAR(30) NOT NULL,
	RFC VARCHAR(13),
	CREDITO DECIMAL(5,2),
	DEUDA DECIMAL(5,2),
	DIRECCION VARCHAR(70) NOT NULL,
	CP INTEGER(5) NOT NULL);

